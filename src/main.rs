#[derive(Debug, Clone, Copy)]
struct PdbAddress {
    section: u16,
    offset: u32,
}

#[derive(Debug)]
struct PeSectionInfo {
    index: u16,
    address: u64,
    size: u64,
}

#[derive(Debug)]
struct AddressMap {
    sections: Vec<PeSectionInfo>,
}

impl AddressMap {
    fn from_pe(pe: &PeFile64<'_>) -> Result<Self> {
        let mut sections = Vec::new();

        for section in pe.sections() {
            let index = u16::try_from(section.index().0)
                .map_err(|_| anyhow::anyhow!("PE section index does not fit in u16"))?;
            let size = section.size();
            if size == 0 {
                continue;
            }

            sections.push(PeSectionInfo {
                index,
                address: section.address(),
                size,
            });
        }

        Ok(Self { sections })
    }

    fn resolve(&self, va: u64) -> Option<PdbAddress> {
        for section in &self.sections {
            let end = section.address.saturating_add(section.size);
            if va < section.address || va >= end {
                continue;
            }

            let offset = va - section.address;
            if offset <= u32::MAX as u64 {
                return Some(PdbAddress {
                    section: section.index,
                    offset: offset as u32,
                });
            }
        }

        None
    }
}

