fn get_pdb_info(args: &PdbArgs, pe: &PeFile64) -> Result<(u32, u32, [u8; 16])> {
    let mut age = 0;
    let mut guid = [0; 16];
    let mut signature = 0;
    if let Some(pdb_info) = pe.pdb_info()? {
        age = pdb_info.age();

        guid = pdb_info.guid();
    }
    if let Some(a) = args.age {
        age = a;
    }
    if let Some(g) = &args.guid {
        guid = uuid::Uuid::parse_str(&g)?.to_bytes_le();
    }
    if let Some(s) = args.signature {
        signature = s;
    }
    Ok((age, signature, guid))
}

fn resolve_function_address(
    address_map: &AddressMap,
    function_va: u64,
    fallback_addr_offset: u64,
) -> Option<(PdbAddress, bool)> {
    if let Some(address) = address_map.resolve(function_va) {
        return Some((address, false));
    }

    function_va
        .checked_sub(fallback_addr_offset)
        .filter(|offset| *offset <= u32::MAX as u64)
        .map(|offset| {
            (
                PdbAddress {
                    section: 1,
                    offset: offset as u32,
                },
                true,
            )
        })
}

fn add_types(
    pdb: &mut PDB,
    il2cpp: &Il2Cpp,
    enum_map: &EnumMap,
    address_map: &AddressMap,
    fallback_addr_offset: u64,
    ida_safe_enums: bool,
    verbose: bool,
) -> Result<AddStats> {
    let mut stats = AddStats::default();
    let mut type_names: Vec<&String> = il2cpp.keys().filter(|name| !name.is_empty()).collect();
    type_names.sort_by(|a, b| {
        inheritance_depth(a.as_str(), il2cpp)
            .cmp(&inheritance_depth(b.as_str(), il2cpp))
            .then_with(|| a.cmp(b))
    });

    if il2cpp.contains_key("") {
        println!("[INFO] Skipping \"\" (empty)");
    }

    for name in &type_names {
        let t = &il2cpp[*name];
        if t.parent != "System.Enum" {
            continue;
        }

        if verbose {
            println!("Adding Type {name}");
        }

        if let Some(val_field) = t.fields.get("value__") {
            let underlying_type = get_pdb_type(&val_field.r#type, il2cpp);
            if let Some(variants) = enum_map
                .get(name.as_str())
                .filter(|variants| !variants.is_empty())
            {
                let variants: Vec<PDBEnumVariant> = variants
                    .iter()
                    .map(|variant| PDBEnumVariant {
                        name: if ida_safe_enums {
                            format!(
                                "{}__{}",
                                sanitize_member_prefix(name),
                                sanitize_member_prefix(&variant.name)
                            )
                        } else {
                            variant.name.clone()
                        },
                        value: variant.value,
                        is_unsigned: variant.is_unsigned,
                    })
                    .collect();
                pdb.insert_enum(name, &underlying_type, &variants)?;
                stats.enums_added += 1;
            } else {
                let fields = vec![StructField {
                    ty: underlying_type,
                    name: "value__".to_string(),
                    offset: 0,
                    is_static: false,
                }];
                pdb.insert_struct(name, &fields, 4)?;
                stats.structs_added += 1;
            }
        }
    }

    for name in &type_names {
        let t = &il2cpp[*name];
        if t.parent == "System.Enum" {
            continue;
        }

        if verbose {
            println!("Adding Type {name}");
        }

        let mut virtual_methods: Vec<&REMethod> = t
            .methods
            .values()
            .filter(|m| m.vtable_index.is_some())
            .collect();

        let is_value_type = t.parent.as_str() == "System.ValueType";

        // value types shouldnt have vtables in their structs
        // TODO: maybe i should add a boxed type with a vtable
        if !virtual_methods.is_empty() && !is_value_type {
            // safe to unwrap here since the above filters by is_some
            virtual_methods.sort_by_key(|m| m.vtable_index.unwrap());
            let vtable_name = format!("{}__vtable", name);
            let mut vtable_fields = vec![];
            let mut max_vtable_size = 0;
            for method in &virtual_methods {
                let v_idx = method.vtable_index.unwrap() as u64;
                let pdb_func = method.get_pdb_function(Some(&name), il2cpp);
                let func_ptr_type = PDBType::Pointer(Box::new(PDBType::Function(pdb_func)));
                let offset = v_idx * 8;
                vtable_fields.push(StructField {
                    ty: func_ptr_type,
                    name: method.name.clone(),
                    offset,
                    is_static: false,
                });
                max_vtable_size = max_vtable_size.max(offset + 8);
            }
            //println!("Adding vtable {vtable_name}");
            pdb.insert_struct(&vtable_name, &vtable_fields, max_vtable_size)?;
            stats.structs_added += 1;

            let (mut main_struct_fields, inherited_fields_added) = t.get_struct_fields(il2cpp)?;
            stats.inherited_fields_added += inherited_fields_added;
            main_struct_fields.push(StructField {
                ty: PDBType::Pointer(Box::new(PDBType::Struct(vtable_name))),
                name: "__vftable".to_string(),
                offset: 0x0,
                is_static: false,
            });
            main_struct_fields.sort_by_key(|f| f.offset);
            pdb.insert_struct(&name, &main_struct_fields, t.size as u64)?;
            stats.structs_added += 1;
        } else {
            //println!("Adding normal struct {}", t.name);
            let (struct_fields, inherited_fields_added) = t.get_struct_fields(il2cpp)?;
            stats.inherited_fields_added += inherited_fields_added;
            pdb.insert_struct(&name, &struct_fields, t.size as u64)?;
            stats.structs_added += 1;
        }
    }

    for name in &type_names {
        let t = &il2cpp[*name];
        for (_f_name, function) in &t.methods {
            if function.function != 0 {
                let signature = function.signature(t);
                if verbose {
                    println!("\tAdding function {}@{:x}", signature, function.function);
                }
                let Some((pdb_address, used_fallback)) =
                    resolve_function_address(address_map, function.function, fallback_addr_offset)
                else {
                    stats.functions_skipped_outside_sections += 1;
                    if verbose {
                        println!(
                            "\tSkipping function {}@{:x}: address is outside PE sections",
                            signature, function.function
                        );
                    }
                    continue;
                };
                if used_fallback {
                    stats.functions_mapped_by_fallback += 1;
                }

                let pdb_func = function.get_pdb_function(Some(&name), il2cpp);
                let func_type = PDBType::Function(pdb_func);
                let sym_name = function.symbol_name(t);
                let mut param_names = vec![];
                param_names.push("vmctx");
                if function.impl_flags.contains("HasThis") {
                    param_names.push("this");
                }

                if let Some(params) = &function.params {
                    for param in params {
                        if param.name.is_empty() {
                            // hopefully the dissassembler renames blank names itself pls and ty
                            param_names.push("");
                        } else {
                            param_names.push(param.name.as_str());
                        }
                    }
                }
                //pdb.insert_function(1, offset_addr, &sym_name, None)?;
                pdb.insert_function(
                    pdb_address.section,
                    pdb_address.offset,
                    &sym_name,
                    Some(&func_type),
                    &param_names,
                )?;
                stats.functions_added += 1;
                //pdb.insert_function(1, offset_addr, &function.name, Some(&func_type))?;
            }
        }
    }

    Ok(stats)
}

fn main() -> Result<()> {
    let args = PdbArgs::parse();

    println!("[INFO] Loading il2cpp from {}", args.il2cpp);
    let il2cpp = std::fs::read_to_string(&args.il2cpp)?;
    let mut deserializer = serde_json::Deserializer::from_str(&il2cpp);
    let mut il2cpp = deserialize_il2cpp(&mut deserializer)?;

    if let Some(re) = &args.filter {
        let regex = Regex::new(&re)?;
        println!("[INFO] Filtering for \"{re}\"");
        il2cpp.retain(|k, _| regex.is_match(&k));
    }
    println!("[INFO] Finished loading il2cpp");

    let enum_map = if let Some(enum_path) = resolve_enum_path(&args) {
        println!("[INFO] Loading enums from {}", enum_path.display());
        let enums = load_enum_map(&enum_path)?;
        println!("[INFO] Finished loading {} enums", enums.len());
        enums
    } else {
        println!("[INFO] No Enums_Internal.hpp found; enum types will use value__ structs");
        EnumMap::new()
    };

    println!("[INFO] Loading exe from {}", args.exe);
    let data = fs::read(&args.exe)?;
    let pe = PeFile64::parse(&*data)?;
    let is_64 = pe.is_64();
    if !is_64 {
        println!("[WARNING] exe is not 64-bit");
    }
    let (age, signature, guid) = get_pdb_info(&args, &pe)?;
    println!(
        "[INFO] age={age}, signature={signature}, guid={}",
        uuid::Uuid::from_bytes_le(guid)
    );

    let mut pdb = PDB::new(is_64, age, signature, guid).unwrap();

    let address_map = AddressMap::from_pe(&pe)?;
    println!(
        "[INFO] Loaded {} PE sections for address mapping",
        address_map.sections.len()
    );
    let start_time = Instant::now();
    println!("[INFO] Adding {} il2cpp types", il2cpp.len());
    let stats = add_types(
        &mut pdb,
        &il2cpp,
        &enum_map,
        &address_map,
        args.address,
        args.ida_safe_enums,
        args.verbose,
    )?;
    println!(
        "[INFO] Added {} structs, {} enums, {} inherited fields, and {} functions in {}s",
        stats.structs_added,
        stats.enums_added,
        stats.inherited_fields_added,
        stats.functions_added,
        start_time.elapsed().as_secs_f32()
    );
    if stats.functions_mapped_by_fallback != 0 {
        println!(
            "[WARNING] Mapped {} functions with the legacy section-1 fallback",
            stats.functions_mapped_by_fallback
        );
    }
    if stats.functions_skipped_outside_sections != 0 {
        println!(
            "[WARNING] Skipped {} functions whose addresses were outside PE sections",
            stats.functions_skipped_outside_sections
        );
    }

    // ye idk wtf this is lol but whatever
    let exe_path = PathBuf::from(&args.exe);
    let mut output = args.output.map(|o| PathBuf::from(o)).unwrap_or_else(|| {
        exe_path
            .parent()
            .map(|x| x.to_path_buf())
            .unwrap_or(PathBuf::new())
    });
    let file_name = args.name.unwrap_or_else(|| {
        exe_path
            .file_prefix()
            .map(|x| x.to_string_lossy().to_string())
            .unwrap_or("generated".into())
    });

    let pdb_file_name = format!("{file_name}.pdb");
    output.push(pdb_file_name);
    let output = output.to_string_lossy().to_string();
    println!("[INFO] Outputting to {output}");
    pdb.commit(&args.exe, &output)?;

    Ok(())
}
