#[derive(Parser, Debug)]
#[command(version, about, long_about = None)]
struct PdbArgs {
    #[arg(
        short,
        long,
        default_value = "il2cpp_dump.json",
        help = "path to il2cpp dump generated by reframework"
    )]
    il2cpp: String,
    #[arg(
        short,
        long,
        help = "path to the game exe, e.g 'C:\\Program Files (x86)\\Steam\\steamapps\\common\\MonsterHunterWilds\\MonsterHunterWilds.exe'"
    )]
    exe: String,
    #[arg(
        short,
        long,
        help = "path to the output directory, e.g 'C:\\Program Files (x86)\\Steam\\steamapps\\common\\MonsterHunterWilds'. If unspecified, it will be written to the directory of the exe"
    )]
    output: Option<String>,
    #[arg(
        short,
        long,
        help = "name of the pdb file (file will be written as \"<name>.pdb\", default uses the name of the exe)"
    )]
    name: Option<String>,
    #[arg(
        short,
        long,
        help = "override for the age that will be written to the pdb file"
    )]
    age: Option<u32>,
    #[arg(
        short,
        long,
        help = "override for the signature that will be written to the pdb file (default=0)"
    )]
    signature: Option<u32>,
    #[arg(
        short,
        long,
        help = "override for the guid that will be written to the pdb file"
    )]
    guid: Option<String>,
    #[arg(short, long, help = "regex filter for types to add")]
    filter: Option<String>,
    #[arg(short, long)]
    verbose: bool,
    #[arg(
        short,
        long,
        // it doesn't work with 0x140000000 and 0th segment, I'm probably just dumb though
        default_value_t = 0x140001000,
        help = "base address of the exe, types get added to the pdb relative to the 1st segment"
    )]
    address: u64,
}

#[allow(unused)]
#[derive(Debug, Deserialize)]
pub struct REType {
    #[serde(skip)]
    name: String,
    #[serde(default, deserialize_with = "parse_address_u64")]
    address: u64,
    #[serde(deserialize_with = "parse_address_u32")]
    crc: u32,
    #[serde(default, deserialize_with = "deserialize_fields")]
    fields: HashMap<String, REField>,
    #[serde(deserialize_with = "parse_address_u64")]
    fqn: u64,
    #[serde(default)]
    is_generic_type: bool,
    #[serde(default)]
    is_generic_type_definition: bool,
    #[serde(default, deserialize_with = "deserialize_methods")]
    methods: HashMap<String, REMethod>,
    #[serde(default)]
    name_hierarchy: Vec<String>,
    #[serde(default)]
    properties: HashMap<String, REProperty>,
    #[serde(deserialize_with = "parse_address_u32")]
    #[serde(default)]
    size: u32,
}

impl REType {
    pub fn get_struct_fields(&self) -> Result<Vec<StructField>> {
        let mut struct_fields = vec![];
        for (f_name, field) in &self.fields {
            if let Ok(ty) = to_pdb_type(&field.r#type) {
                let sf = StructField {
                    ty,
                    name: f_name.clone(),
                    offset: field.offset_from_base as u64,
                    is_static: field.flags.contains("Static"),
                };
                struct_fields.push(sf);
            }
        }
        struct_fields.sort_by_key(|f| f.offset);
        Ok(struct_fields)
    }
}

#[allow(unused)]
#[derive(Debug, Deserialize)]

pub struct REField {
    #[serde(skip)]
    name: String,
    id: u32,
    init_data_index: u32,
    #[serde(deserialize_with = "parse_address_u32")]
    offset_from_base: u32,
    #[serde(deserialize_with = "parse_address_u32")]
    offset_from_fieldptr: u32,
    r#type: String,
    #[serde(default)]
    flags: String,
}

pub fn to_pdb_type(name: &str) -> Result<PDBType> {
    let t = match name {
        "System.Boolean" => PDBType::SimpleType(SimpleTypeKind::Boolean8),
        "System.SByte" => PDBType::SimpleType(SimpleTypeKind::SByte),
        "System.Int16" => PDBType::SimpleType(SimpleTypeKind::Int16),
        "System.Int32" => PDBType::SimpleType(SimpleTypeKind::Int32),
        "System.Int64" => PDBType::SimpleType(SimpleTypeKind::Int64),
        "System.Byte" => PDBType::SimpleType(SimpleTypeKind::Byte),
        "System.UInt16" => PDBType::SimpleType(SimpleTypeKind::UInt16),
        "System.UInt32" => PDBType::SimpleType(SimpleTypeKind::UInt32),
        "System.UInt64" => PDBType::SimpleType(SimpleTypeKind::UInt64),
        "System.Single" => PDBType::SimpleType(SimpleTypeKind::Float32),
        "System.Double" => PDBType::SimpleType(SimpleTypeKind::Float64),
        "System.Char" => PDBType::SimpleType(SimpleTypeKind::WideCharacter),
        "System.Void" => PDBType::SimpleType(SimpleTypeKind::Void),
        "System.Guid" => {
            PDBType::ConstantArray(Box::new(PDBType::SimpleType(SimpleTypeKind::Byte)), 8)
        }
        _ => PDBType::Pointer(Box::new(PDBType::Struct(name.to_string()))),
        //_ => bail!("Unmatched type"),
    };
    Ok(t)
}

#[allow(unused)]
#[derive(Debug, Deserialize)]
pub struct REMethod {
    #[serde(skip)]
    name: String,
    flags: Option<String>,
    #[serde(deserialize_with = "parse_address_u64")]
    function: u64,
    id: u32,
    #[serde(default)]
    impl_flags: String,
    invoke_id: u32,
    params: Option<Vec<REParam>>,
    returns: Option<REParam>,
    vtable_index: Option<u32>,
}

impl REMethod {
    pub fn symbol_name(&self, r#type: &REType) -> String {
        format!("{}::{}", r#type.name, self.name)
    }
    pub fn signature(&self, r#type: &REType) -> String {
        let params = self
            .params
            .as_ref()
            .map(|p| {
                let mut params = String::new();
                for (i, param) in p.iter().enumerate() {
                    params.push_str(&param.r#type);
                    if !param.name.is_empty() {
                        params.push(' ');

                        params.push_str(&param.name);
                    }
                    if i != p.len() {
                        params.push(',');
                    }
                }
                params
            })
            .unwrap_or("".to_string());
        let signature = format!("{}({})", self.symbol_name(r#type), params);
        signature
    }

    pub fn get_pdb_function(&self, class: Option<&str>) -> PDBFunction {
        let ret_type = self
            .returns
            .as_ref()
            .and_then(|f| to_pdb_type(&f.r#type).ok())
            // this never really happens
            .unwrap_or_else(|| PDBType::SimpleType(SimpleTypeKind::Void));

        // vmctx passed in here
        let mut param_types = vec![PDBType::Pointer(Box::new(PDBType::SimpleType(
            SimpleTypeKind::Void,
        )))];

        if self.impl_flags.contains("HasThis") {
            if let Some(class_name) = class {
                param_types.push(PDBType::Pointer(Box::new(PDBType::Struct(
                    class_name.to_string(),
                ))));
            }
        }

        if let Some(params) = &self.params {
            for param in params {
                let pdb_type = to_pdb_type(&param.r#type)
                    .ok()
                    .unwrap_or_else(|| PDBType::SimpleType(SimpleTypeKind::Void));
                param_types.push(pdb_type);
            }
        }

        let cconv = CallingConvention::NearFast;

        //let class_type = class.map(|x| PDBType::Struct(x.to_string()));
        //PDBFunction::new(ret_type, &param_types, class_type, cconv)
        PDBFunction::new_ex(ret_type, param_types, None, cconv)
    }
}

